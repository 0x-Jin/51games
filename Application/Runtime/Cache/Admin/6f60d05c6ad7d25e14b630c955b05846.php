        function backstageLogin(id) {
            $(".mark").show();
            $(".spinner").show();
            $.ajax({
                type: "GET",
                url: "<?php echo U('Backstage/loginToken');?>",
                data: {id: id},
                dataType: "json",
                success: function(data) {
                    if (data.Result == 1) {
                        $("#content").html(data.Html).show();
                        $(".mark").hide();
                        $(".spinner").hide();
                    } else {
                        showMsg("warning", data.Msg);
                        $(".mark").hide();
                        $(".spinner").hide();
                    }
                },
                error: function() {
                    showMsg("warning", "数据请求失败！");
                    $(".mark").hide();
                    $(".spinner").hide();
                }
            })
        }

        //显示提示
        function showMsg(type, msg) {
            BUI.Message.Show({
